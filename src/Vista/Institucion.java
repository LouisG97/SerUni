    public void iniciar(){
        InstitucionC i = new InstitucionC();
        i.setIdDeLaInstitucion("1");
        try {
            i.cargarDatos2(SQL.ejecutaQ(i.buscarID(), this,0));
        } catch (SQLException ex) {
        }
        jtfNombre.setText(i.getNombreDeLaInstitucion());
        jtfDireccion.setText(i.getDireccion());
        jtfTelefono.setText(i.getTelefono());
        jtfResponsable.setText(i.getResponsableDeLaInstitucion());
        jLabel2.setIcon(null);
        jLabel5.setIcon(null);
        cargarImagen3(i.getImagen1());
        cargarImagen4(i.getImagen2());
    }
    
    public void Modificar(){
        InstitucionC i = new InstitucionC();
        i.setIdDeLaInstitucion("1");
        try {
            i.cargarDatos2(SQL.ejecutaQ(i.buscarID(), this,0));
        } catch (SQLException ex) {
        }
        if(diImagen.compareTo("NA")!=0){
            i.setImagen1(diImagen);
        }
        if(diImagen2.compareTo("NA")!=0){
            i.setImagen2(diImagen2);
        }
        i.setResponsableDeLaInstitucion(jtfResponsable.getText());
        i.setTelefono(jtfTelefono.getText());
        String sql="UPDATE `ponguibd`.`institucion` SET `imagen1` = '"+dobleImagen(i.getImagen1())+"', `imagen2` = '"+dobleImagen(i.getImagen2())+
                "', `nombreDeLaInstitucion` = '"+jtfNombre.getText()+"', `direccion` = '"+jtfDireccion.getText()+
                "', `telefono` = '"+jtfTelefono.getText()+"', `responsableDeLaInstitucion` = '"+jtfResponsable.getText()+
                "' WHERE (`idDeLaInstitucion` = '1');";
        SQL.ejecuta(sql,this,0);
        System.out.println(sql);
        iniciar();
    }
    
    public String dobleImagen(String txt){
        String doble="";
        for(int a = 0;a<txt.length();a++){
            String ejemplo = txt.charAt(a)+"";
            if(ejemplo.compareTo("\\")==0){
                doble=doble+"\\";
            }
            doble=doble+ejemplo;
        }
        return doble;
    }
    
