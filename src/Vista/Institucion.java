    public void cargarImagen3(String txt){
        ImageIcon fot = new ImageIcon(txt);
        int w = fot.getIconWidth();
        int h = fot.getIconHeight();
        if(w>=(h*4)){
            w=200;
            h=20;
        }
        else if(w>=(h*3)){
            w=200;
            h=50;
        }
        else if(w>=(h*2)){
            w=200;
            h=100;
        }
        else{
            w=100;
            h=90;
        }
        jlImagen1.setSize(w, h);
        Icon icono = new ImageIcon(fot.getImage().getScaledInstance(jlImagen1.getWidth(), jlImagen1.getHeight(), Image.SCALE_DEFAULT));
        jlImagen1.setIcon(icono);
        this.repaint();
    }
