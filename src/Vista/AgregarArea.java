    private void jtfBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfBuscarKeyReleased
        String s;
        AreasC n = new AreasC();
        try{
        s = n.buscarID("nombreDelArea",jtfBuscar.getText());
        n.cargarDatos(SQL.ejecutaQ(s, this,0),(DefaultTableModel)tabla.getModel());
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Problema de captura", "Mensaje de error", JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jtfBuscarKeyReleased

    private void tablaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseReleased
        int r = tabla.rowAtPoint(evt.getPoint());
        String nombre = tabla.getValueAt(r, 0)+"";
        
        AreasC u = new AreasC();
        u.setNombreDelArea(nombre);
        int error=0;
        try {
            u.cargarDatos2(SQL.ejecutaQ(u.buscarID("nombreDelArea",nombre), this,0));
        } catch (SQLException ex) {
        }
        
        jtfNombre.setText(nombre);
        jtfTelefono.setText(u.getTelefono());
        jtfExtension.setText(u.getExtTelefono());
        jtfEncargado.setText(u.getEncargado());
        
        nombreTemporal=nombre;
    }//GEN-LAST:event_tablaMouseReleased

