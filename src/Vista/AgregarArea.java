    public void Agregar(){
        String sql="INSERT INTO `ponguibd`.`areas` (`nombreDelArea`, `telefono`, `extTelefono`, `encargado`) "
                + "VALUES ('"+jtfNombre.getText()+"', '"+jtfTelefono.getText()+"', '"+jtfExtension.getText()+
                "', '"+jtfEncargado.getText()+"');";
        SQL.ejecuta(sql,this,0);
        iniciar();
    }
    public void Modificar(){
        String sql="UPDATE `ponguibd`.`areas` SET `nombreDelArea` = '"+jtfNombre.getText()+"', `telefono` = '"+jtfTelefono.getText()+"', "
                + "`extTelefono` = '"+jtfExtension.getText()+"', `encargado` = '"+jtfEncargado.getText()+"' WHERE (`nombreDelArea` = '"+nombreTemporal+"');";
        SQL.ejecuta(sql,this,0);
        System.out.println(sql);
        iniciar();
    }
    public void Eliminar(){
        String sql = "DELETE FROM `ponguibd`.`areas` WHERE (`nombreDelArea` = '"+jtfNombre.getText()+"');";
        SQL.ejecuta(sql,this,0);
        System.out.println(sql);
        iniciar();
    }
    
    
    
    public void iniciar(){
        String s;
        AreasC n = new AreasC();
        try{
        s = n.buscarID("nombreDelArea","");
        n.cargarDatos(SQL.ejecutaQ(s, this,0),(DefaultTableModel)tabla.getModel());
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Problema de captura", "Pagina de error", JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }
    
    public void datos(String n, String p, String u, String a, String c){
        nombre=n;
        prioridad=p;
        usuario=u;
        area=a;
        contraseña=c;
    }
    
