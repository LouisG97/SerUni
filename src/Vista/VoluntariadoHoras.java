    public void iniciar(String id2){
        id=id2;
        VoluntariosC u = new VoluntariosC();
        u.setId(id);
        try {
            u.cargarDatos2(SQL.ejecutaQ(u.buscarID(), this,0));
        } catch (SQLException ex) {
        }
        jtfClave.setText(u.getId());
        jtfNombre.setText(u.getNombreCompleto());
        jcbArea.setSelectedItem(u.getArea());
        double kilos = Double.parseDouble(u.getKilosR());
        double kilos2 = Double.parseDouble(u.getKilos());
        double horas = Double.parseDouble(u.getHorasContadas());
        double horasTotales = Double.parseDouble(u.getHorasPlan());
        lbKilosTotales.setText(kilos2+"");
        lbHorasTotales.setText(horas+"");
        lbKilosRestantes.setText((kilos-kilos2)+"");
        lbHorasRestantes.setText((horasTotales-horas)+"");
        
    }
    
