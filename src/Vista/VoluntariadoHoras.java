    private void btnAceptarHorasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarHorasActionPerformed
        int bandera = 0;
        
        id=jtfClave.getText();
        VoluntariosC u = new VoluntariosC();
        u.setId(id);
        try {
            u.cargarDatos2(SQL.ejecutaQ(u.buscarID(), this,0));
        } catch (SQLException ex) {
        }
        
        if(rb2.isSelected()){
            bandera = 1;
        }
        else{
            bandera = 0;
        }
        double horas = Double.parseDouble(tfCantidad.getText());
        double totales = Double.parseDouble(lbHorasTotales.getText());
        double restantes = Double.parseDouble(lbHorasRestantes.getText());
        if(bandera==0){
            totales = totales+horas;
            restantes = restantes-horas;
        }
        else if(bandera==1){
            totales = totales-horas;
            restantes = restantes+horas;
        }
        
        u.setHorasContadas(totales+"");
        
        String sql="UPDATE `ponguibd`.`voluntarios` SET `horasContadas` = '"+u.getHorasContadas()+"' WHERE (`id` = '"+u.getId()+"');";
        SQL.ejecuta(sql,this,0);
        iniciar(id);
    }//GEN-LAST:event_btnAceptarHorasActionPerformed

